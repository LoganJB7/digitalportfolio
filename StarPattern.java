/* This project consists of a pattern called stars that is being printed 
in a specific order,
*/

public class StarPattern
{
   public static void main(String[] args)
   {
      System.out.println("   *");
      System.out.println("  ***");
      System.out.println(" *****");
      System.out.println("*******");
      System.out.println(" *****");
      System.out.println("  ***");
      System.out.println("   *");
   }
}